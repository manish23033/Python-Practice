x = float(input("1st no."))
y = float(input("2st no."))
z = input("enter the  operator{/,*,-,+,//}")
if z=="+":
    print("sum of two no.", x+y)
elif z=="-":
    print("subtract of two no.", x-y)
elif z=="*":
    print("multiply of two no.", x8y)
elif z=="/":
    print("division of two no.", x/y)
elif z=="//":
    print("floor division of two no.", x//y)
else:
    print("wrong choice")    

